function [Vwind] = windfield(h, windtype) % h m

if h < 0
    h = 0;
elseif h > 30000
    h = 30000;
end

Vwind = zeros(3,1) ;
switch windtype
    case 1 % ÃÀ¹ú h km
        h = h/1000;
        vc = 395.04; % m/s
        dc = 15891.915; % km
        ce = [-0.16235, 0.83550, -1.64017, 1.60473, -0.89284, 0.20646, 0.09708, 0.00246]';
        cn = [-0.00103, 0.00502, -0.00789, 0.00459, -0.00104, -0.00085, 0.00047, -0.0001]';
        Vwind(1) = vc*(ce(1) + ce(2)*(h/dc) + ce(3)*(h/dc)^2 + ce(4)*(h/dc)^3 + ce(5)*(h/dc)^4 + ce(6)*(h/dc)^5 + ce(7)*(h/dc)^6 + ce(8)*(h/dc)^7);
        Vwind(2) = vc*(cn(1) + cn(2)*(h/dc) + cn(3)*(h/dc)^2 + cn(4)*(h/dc)^3 + cn(5)*(h/dc)^4 + cn(6)*(h/dc)^5 + cn(7)*(h/dc)^6 + cn(8)*(h/dc)^7);
    case 2 % º«¹ú h m
        h = (h - 12135)/9022;
        Vwind(1) = 3.770*h^7-12.5587*h^6-9.1512*h^5+50.2420*h^4+6.3696*h^3-73.9562*h^2-4.4017*h+42.6254; % E
        Vwind(2) = 4.5571*h^7-4.8683*h^6-16.180*h^5+12.816*h^4+17.7469*h^3-7.6892*h^2-3.3630*h+4.5189; % N
    otherwise
end
